span = document.getElementById("ep1");
span.onclick = function () {
  window.scrollTo({
    top: 0,
    behavior: "smooth",
  });
  {
    video.src =
      "https://archive.org/download/friends.s10e17e18.720p.bluray.sujaidr/friends.s10e01.720p.bluray.sujaidr.mkv";
    video.play();
  }
  {
    episode.innerHTML = 1;
  }
  document.getElementById(
    "en"
  ).src = `assets/s10 en/s10 (${episode.innerHTML}).vtt`;
  document.getElementById(
    "ar"
  ).src = `assets/s10 ar/s10 (${episode.innerHTML}).vtt`;
};
